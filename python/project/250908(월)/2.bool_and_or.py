# and 연산자 or 연산자
# and : 두 개의 조건이 모두 True 일 때만 전체 평가가 True
# ~이고 ~이다
# 평균이 60 이상이고 각 과목별 점수가 40 이상이면 합격

# or : 두 중에 한 개의 조건만 True 일 때만 전체 평가가 True
# ~이거나 ~또는 ~이다
# 60세 이상이거나 5세 미만이면 입장료 무료

# 이진법으로 표현하면 0 이 나올때는 False 나머지 숫자는 모두 True
print(10 >0 and 10>100) # 1 X 0 = 0 
print(10 >0 or 10>100) # 1 + 0 =  1