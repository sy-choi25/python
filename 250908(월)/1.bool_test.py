# 불 자료형 - True와 False 형태만 가지고 있는 변수
print(f'True {True}')
print(f'False{False}')
print(f' 10 > 0{10>0}' )
print(f' 10 >= 0 {10>=0}' )
print(f' 10 < 0 {10 <0}')
print(f' 10 <= 0 { 10 <= 0}')
print( f'10 == 0 { 10 == 0}') # 같다는 등호 두개로 표시
# not 
print(f'not True{not True}')
print(f'not 10 == 0{ not 10 == 0}')